
public class Pessoa {
	private String nome;
	private String endereco;
	private String cpf;
	private String naturalidade;
	private int idade;
	
	public Pessoa(String nome, String endereco, String cpf, String naturalidade, int idade){
		this.nome = nome;
		this.endereco = endereco;
		this.cpf = cpf;
		this.naturalidade = naturalidade;
		this.idade = idade;
	}

	
}
